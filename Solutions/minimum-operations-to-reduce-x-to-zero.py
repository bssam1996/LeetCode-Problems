def minOperations(nums, x):
    totalsum = sum(nums)
    subarraysum = totalsum - x
    # corner cases
    if subarraysum == 0:
        return len(nums)
    if subarraysum < 0:
        return -1
    L = 0
    T = 0
    sublen = -1
    for R in range(len(nums)):
        T += nums[R]
        while T >= subarraysum and L <= R:
            if T == subarraysum:
                sublen = max(sublen, R - L + 1)
            T -= nums[L]
            L += 1
    if sublen == -1:
        return -1

    return len(nums) - sublen




